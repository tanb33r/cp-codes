void solve() {
    ll a,b,x,y,n;
    cin>>a>>b>>x>>y>>n;
    int q = max(x,a-n);
    int w = max(y,b-n);

    if(q<w) {
      n-=a-q;
      a=q;
      b -= min(n,b-y);
    }
    else  {
      n-=b-w;
      b=w;
      a-=min(n,a-x);
    }
    cout<<a*b<<endl;

}


