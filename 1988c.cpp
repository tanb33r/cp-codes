const int mod = 1e9+7;
const int N = 1e5+7;

void solve() {
    ll n;
    cin>>n;
    string s  = "";
    ll n2= n;
    while(n2) {
        s += n2 % 2 ?'1':'0';
        n2/=2;
    }
    reverse(all(s));
    vector<ll> v;
    v.pb(stoll(s, nullptr, 2));
    int N = s.size();
    for(int i = N-1; i>=0; i--) {
        if(s[i] == '1') {
            string s3 = s;
            s3[i]='0';
            ll num = stoll(s3, nullptr, 2);
            if(num!=0)
                v.pb(stoll(s3, nullptr, 2));
        }
    }
    reverse(all(v));
    pr(v.size());
    vout(v);
}

