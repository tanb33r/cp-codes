string get(string s, int k) {
    while((int)s.size() < k) {
        s = s + s;
    }
    while((int)s.size() > k)
        s.pop_back();
    return s;
}

void solve() {
    int n,k=0;
    string s;
    cin>>n>>k>>s;

    string ans = "";
    ans += s[0];

    string mn = get(ans, k);

    for(int i = 1; i < n; i++) {
        if(s[i] > s[0])break;
        ans += s[i];
        mn = min(mn, get(ans, k));
    }
    pr(mn);

}
