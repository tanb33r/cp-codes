const int mod = 1e9+7;

ll N1,N2,K1,K2;
map<vector<int>, ll> m;
ll dp(ll n1, ll n2, ll k1, ll k2) {
    if(n1<0 or n2<0 or k1>K1 or k2>K2)
        return 0;
    if(n1==0 and n2==0)
        return 1;

    vector<int> v = {n1,n2,k1,k2};
    if(!m.count(v))
        m[v] = (dp(n1-1,n2, k1+1,0)+dp(n1,n2-1,0, k2+1))%((ll)1e8);
    return m[v];
}

void solve() {
    cin>>N1>>N2>>K1>>K2;
    pr(dp(N1,N2,0,0));
}

